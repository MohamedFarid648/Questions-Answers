        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult Create([Bind(Include = "id,text,QuestionID")] Answer answer)
        {
            if (ModelState.IsValid)
            {
                User user = null;
                if (Session["UserSessionEmail"] != null)
                {


                    string user_email = Session["UserSessionEmail"].ToString();
                    user = db.Users.Single(u => u.Email == user_email);
                    answer.UserID = user.ID;
                    db.Answers.Add(answer);
                    db.SaveChanges();
                    return RedirectToAction("Details","Questions",answer.QuestionID);
                }
                else
                {
                    return RedirectToAction("Login", "Users");
                }
               
            }
            return View(answer);

        }
